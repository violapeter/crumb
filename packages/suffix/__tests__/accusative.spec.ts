    expect(suffix.accusative('delizsánsz')).toBe('delizsánszot')
    expect(suffix.accusative('toll')).toBe('tollat')
    expect(suffix.accusative('szakáll')).toBe('szakállat')
    expect(suffix.accusative('váladék')).toBe('váladékot')
    expect(suffix.accusative('szobor')).toBe('szobrot')
    expect(suffix.accusative('fédervejsz')).toBe('fédervejszet')
    expect(suffix.accusative('kapocs')).toBe('kapcsot')
    expect(suffix.accusative('tanács')).toBe('tanácsot')
