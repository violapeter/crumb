    expect(suffix.accusative('pajzs')).toBe('pajzsot')
    expect(suffix.accusative('őrs')).toBe('őrsöt')
    expect(suffix.accusative('enyv')).toBe('enyvet')
    expect(suffix.accusative('törzs')).toBe('törzset')
    expect(suffix.accusative('torzs')).toBe('torzsot')
    expect(suffix.accusative('váll')).toBe('vállat')
    expect(suffix.accusative('gally')).toBe('gallyat')
    expect(suffix.accusative('genny')).toBe('gennyet')
    expect(suffix.accusative('orr')).toBe('orrot')
    expect(suffix.accusative('könny')).toBe('könnyet')
    expect(suffix.accusative('mell')).toBe('mellet')
    expect(suffix.accusative('menny')).toBe('mennyet')
    expect(suffix.accusative('sansz')).toBe('sanszot')
    expect(suffix.accusative('sánsz')).toBe('sánszot')
    expect(suffix.accusative('sánc')).toBe('sáncot')
    expect(suffix.accusative('szeánsz')).toBe('szeánszot')
    expect(suffix.accusative('szenny')).toBe('szennyet')
    expect(suffix.accusative('csimpánz')).toBe('csimpánzt')

    expect(suffix.accusative('rács')).toBe('rácsot')
    expect(suffix.accusative('abroncs')).toBe('abroncsot')
    expect(suffix.accusative('szárny')).toBe('szárnyat')
    expect(suffix.accusative('árny')).toBe('árnyat')
    expect(suffix.accusative('szörny')).toBe('szörnyet')
    expect(suffix.accusative('arany')).toBe('aranyat')
    expect(suffix.accusative('kulcs')).toBe('kulcsot')
